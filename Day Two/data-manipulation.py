#Rounding Off

print(8 / 3)

#Rounds off to the nearest whole number
print(round(8 / 3))

#Rounds off to two d.p
print(round(8 / 3, 2))

#Rounds off to three d.p
print(round(8 / 3, 3))

#Gives answer as an integer automatically
print(8 // 3)
print(8 // 2)

